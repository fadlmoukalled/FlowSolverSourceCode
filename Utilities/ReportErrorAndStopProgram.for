c
C#############################################################################################
      SUBROUTINE nrerror(string)
C#############################################################################################
      implicit none
c*********************************************************************************************
      character(LEN=*), intent(in) :: string
c*********************************************************************************************
c
c  Report a message, then die.
c
c*********************************************************************************************
c
      write (*,*) "nrerror: ",string
      stop "program terminated by nrerror"
c
      return
      end
