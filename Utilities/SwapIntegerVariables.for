c
C#############################################################################################
      SUBROUTINE swapINvariables(a,b)
C#############################################################################################
c---  Swap the contents of a and b.
c*********************************************************************************************
      implicit none
c*********************************************************************************************
      integer, intent(INOUT) :: a,b
      integer :: dum
c*********************************************************************************************
c
      dum=a
      a=b
      b=dum
c
      return
      end
